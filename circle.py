import math


def area(r):
    '''
    Calculates circle area
    ARGUMENTS:
        r - circle radius
    RETURNS:
        circle area
    EXAMPLE:
        >>> area(2)
        12.56637061
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Calculates circle area
    ARGUMENTS:
        r - circle radius
    RETURNS:
        circle area
    EXAMPLE:
        >>> perimeter(2)
        12.56637061
    '''
    return 2 * math.pi * r

